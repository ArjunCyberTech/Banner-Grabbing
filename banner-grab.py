def banner(ip_addr,port):
    soc = socket.socket()
    soc.connect((ip_addr,port))
    banner =str(soc.recv(1024)).strip('b')
    print(banner)

def main():
    ip_addr = input("Enter The IP/Domain: ")
    port = int(input("Enter the Port number: "))
    banner(ip_addr, port)

